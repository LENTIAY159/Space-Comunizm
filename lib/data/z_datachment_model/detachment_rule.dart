/// Представляет дополнительное правило для Detachment
class AdditionalRule {
  /// Название дополнительного правила
  final String name;

  /// Описание дополнительного правила
  final String description;

  /// Конструктор для AdditionalRule
  AdditionalRule({
    required this.name,
    required this.description,
  });
}